    gfx_draw_text("Goodbye!", 220, 250, GS_SETREG_RGBAQ(0xFF,0xA0,0xA0,0x00,0x00), 4, 3);
    gfx_exec();
    gfx_flip();

    FuckAroundSilentlyMs(1500);  // Let user read it
	
	SifInitRpc(0);
	SifIopReset("", 0);
	while(!SifIopSync());

	SifInitRpc(0);
	SifLoadFileInit();
	SifInitIopHeap();
	sbv_patch_enable_lmb();

	SifExecModuleBuffer(poweroff_irx, size_poweroff_irx, 0, NULL, NULL);
